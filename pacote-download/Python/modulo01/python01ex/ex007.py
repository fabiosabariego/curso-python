# Programa para ler a média de 2 notas de um aluno, e calcular a média delas

n1 = int(input('Nota 1: '))
n2 = int(input('Nota 2: '))

media = (n1+n2) / 2

print('As notas do aluno foram {} e {}, e sua média foi de {}'.format(n1, n2, media))